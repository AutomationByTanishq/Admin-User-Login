    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        accn.setEditable(false);
        accn.setEnabled(false);
        name.setEditable(false);
        name.setEnabled(false);
        gen.setEditable(false);
        gen.setEnabled(false);
        id.setEditable(false);
        id.setEnabled(false);
        pwd.setEditable(false);    
        pwd.setEnabled(false); 
        
        try {
               Class.forName("com.mysql.jdbc.Driver");
               conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/Internship","root","");
               Statement stmt = conn.createStatement(); 
               String query=("SELECT id, user_name, user_id, user_password, Mobile_No, Gender, City, State from signin where id='"+ii+"'");
               rs = stmt.executeQuery(query);
               if(rs.next()) 
                   {
                      int ii= rs.getInt("id");
                      String un = rs.getString("user_name");
                      String uuid = rs.getString("user_id");
                      String paw = rs.getString("user_password");
                      String mob = rs.getString("Mobile_No");
                      String gend = rs.getString("Gender");
                      String city = rs.getString("City");
                      String state = rs.getString("State");
                     
                      
                      accn.setText(""+ii);
                      name.setText(un);
                      gen.setText(gend);
                      pwd.setText(paw);
                      id.setText(uuid);
                      mn.setText(mob);
                      ct.setText(city);
                      st.setText(state);
                   }
              }
      
       catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(UserDetails.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formWindowOpened

