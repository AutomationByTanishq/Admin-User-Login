    private void edtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_edtActionPerformed
       
        int p = JOptionPane.showConfirmDialog(null, "Are you sure you want to update?","Update Record",JOptionPane.YES_NO_OPTION);
        if(p==0){
          try{
                
        
                String value4 = mn.getText();
                String value6 = gen.getText();
                String value7 = ct.getText();
                String value8 = st.getText();
                
                String sql= "update signin set  Mobile_no='"+value4+"' , City='"+value7+"', State='"+value8+"' where id='"+accn.getText()+"'";
                pst=conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Record Updated");
                new Internship.LogInn().setVisible(true);
                this.dispose();

            }catch(SQLException | HeadlessException e){
                JOptionPane.showMessageDialog(null, e);
            }
            
            

        }
    }//GEN-LAST:event_edtActionPerformed

